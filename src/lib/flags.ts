// La función normalize() quita acentos, así "México" → "mexico".

/** Normaliza: minúsculas, sin acentos, sin espacios extra. */
function normalize(name: string): string {
  return name
    .toLowerCase()
    .normalize("NFD")
    .replace(/[̀-ͯ]/g, "")
    .trim();
}

